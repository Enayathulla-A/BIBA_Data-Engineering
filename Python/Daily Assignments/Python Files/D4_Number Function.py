import math
a=50
#log() ==> Gives log value of the number
print(math.log(a))
#floor() ==> Gives nearest small value for the given number
b=20.55
c=19.16
d=-21.3
print(math.floor(b))
print(math.floor(c))
print(math.floor(d))
#ceil() ==> Gives nearest large value for the given number 
print(math.ceil(b))
print(math.ceil(c))
print(math.ceil(d))